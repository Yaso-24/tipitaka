<p class="bodytext">“I have made use of this simile, bhikkhus, to illustrate the meaning. And this is the meaning here: ‘The current of the river’ is a synonym for craving. ‘Seeming pleasant and agreeable’ is a synonym for the six internal sense-bases. ‘The pool lower down’ is a synonym for the five lower fetters.‘Turbulent waves’ is a synonym for anger and frustration. ‘Swirling eddies’ is a synonym for the five strands of sensual pleasure. ‘Monsters and demons’ is a synonym for womenfolk. ‘Against the current’ is a synonym for renunciation. ‘Struggling with hands and feet’ is a synonym for instigating energy. ‘The keen-sighted man standing on the bank’ is a synonym for the Tathāgata, the Arahant, the Fully Enlightened One.”</p>
