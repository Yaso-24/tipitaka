<p class="bodytext"><a name="para258"></a><span class="paranum">২৫৮</span>. ‘‘ৰীসতি <a name="M0.0242"></a> দসেসু বা দ্বিস্স তূ’’তি দ্বিস্স বা। বাৰীসতি, একাদসন্নং পূরণো একাদসমো।</p>
