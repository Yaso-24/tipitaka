<p class="bodytext">সম্ভাৰনে গম্যমানে ধাতুনা ৰুচ্চমানে চ এয্যাদযো হোন্তি নৰিভাসা-অপি ভৰং গিলিতং পাসাণং পচেয্য উদরগ্গিনা-সম্হাৰেমি সদ্দহামি ভৰং পচেয্য,ভৰং পচিস্সতি। ভৰং অপচি।</p>
