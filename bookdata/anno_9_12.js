<p class="bodytext">ৰিসেসনং স্যাদ্যন্তং ৰসেস্সেন স্যাদ্যন্তেন সমানাধিকারণেন সহেকত্থং হোতি-নীলঞ্চ তং উপ্পলং চেতি নিলুপ্পলং-ৰাক্যে তুল্যাধিকরণভাৰপ্পকাসনত্থং চতসদ্দপ্পযোগো-ৰুত্তিযন্তু সমাসেনেৰ তপ্পকাসনতো ন তপ্পযোগো-এৰ মঞ্ঞত্রাপি ৰুত্তট্ঠানমপ্পযোগো-বহুলাধিকারা ক্বচি উপমানভুতং ৰিসেসনম্পরং ভৰতি-সীহো’চ সীহো-মুনি চ সো সীহো চাতি মুনিসীহো-মুনিসদ্দোযেৰ ৰা ৰিসেসনং-তথাহি-সীহোতি ৰুত্তে উপচরিতানুপচরিতসীহানং সামঞ্ঞপ্পতীতিযং মুনিসদ্দো ৰিসেসেতি-সীলমেৰ ধনং সীলধনং-ধম্মোতি সম্মতো ধম্মসম্মতো-মহন্তি চ সা সদ্ধা চাতি সমাসে কতে-‘‘ইত্থিযম্ভাসিতপুমিত্থি পুমেৰেকত্থ’’ত পুম্ভাৰা ঙীপ্পচ্চযাভাৰো-‘‘টন্তন্তুনন্তি’’ন্তস্স টে-‘‘ব্যঞ্জনে দীঘরস্সা’’তি দিঘে-মহাসদ্ধা।</p>
