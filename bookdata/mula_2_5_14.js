<p class="gathalast">সো দূসকো রোসকো চাপি জম্মো, সচেৰজেমম্পি <span class="note">[সচে + আৰজে + ইমম্পি]</span> দূসেয্যগারন্তি <span class="note">[দূসযে ঘরন্তি (সী॰ স্যা॰ পী॰)]</span>॥</p>
